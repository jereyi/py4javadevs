public class Solution {

  public static void main(String[] args) {
    double num = 3.14;

    if (num > 0) {
      System.out.println(num + " is positive.");
    } else if (num < 0) {
      System.out.println(num + " is negative.");
    } else {
      System.out.println("The number is zero.");
    }
  }
}
